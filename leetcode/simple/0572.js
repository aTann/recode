// 572. 另一棵树的子树
// https://leetcode-cn.com/problems/subtree-of-another-tree/
/**
 * Definition for a binary tree node.
 * function TreeNode(val, left, right) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.left = (left===undefined ? null : left)
 *     this.right = (right===undefined ? null : right)
 * }
 */
/**
 * @param {TreeNode} root
 * @param {TreeNode} subRoot
 * @return {boolean}
 */
// O(|s|*|t|)/O(max{ds, dt})
var isSubtree = function (root, subRoot) {
  const check = (n, sub) => {
    if (!n && !sub) {
      return true;
    }

    if ((n && !sub) || (!n && sub) || n.val !== sub.val) {
      return false;
    }

    return check(n.left, sub.left) && check(n.right, sub.right);
  };

  const dfs = (node, subNode) => {
    if (!node) {
      return false;
    }

    return (
      check(node, subNode) ||
      check(node.left, subNode.left) ||
      check(node.right, subNode.right)
    );
  };

  return dfs(root, subRoot);
};

// 将 root、subRoot 进行 JSON 格式化，然后进行字符串比较
// O(1)/O(1)
// 76ms/47.7MB
var isSubtree = function (root, subRoot) {
  return JSON.stringify(root).indexOf(JSON.stringify(subRoot)) > -1;
};

// 深度优先搜索序列上做串匹配（KMP/Rabin-Karp）
// 树哈希

function create(list) {
  const tree = { root: {} };
  const root = tree.root;
  const nodes = [root];
  while (list.length) {
    const val = list.shift();
    const node = nodes.shift();
    if (node.val === void 0) {
      node.val = val;
      nodes.unshift(node);
    } else if (node.left === void 0) {
      if (val !== null) {
        node.left = { val };
        nodes.push(node.left);
      } else {
        node.left = val;
      }
      nodes.unshift(node);
    } else if (node.right === void 0) {
      if (val !== null) {
        node.right = { val };
        nodes.push(node.right);
      } else {
        node.right = val;
      }
    }
  }

  return tree.root;
}

